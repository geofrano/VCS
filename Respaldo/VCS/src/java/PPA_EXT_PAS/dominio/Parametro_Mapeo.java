public class Parametro_Mapeo {
    
    String id_parametro;
    String id_actividad;
    String desc_actividad;
    String id_horas;
    String desc_horas;

    public String getId_parametro() {
        return id_parametro;
    }

    public void setId_parametro(String id_parametro) {
        this.id_parametro = id_parametro;
    }

    public String getId_actividad() {
        return id_actividad;
    }

    public void setId_actividad(String id_actividad) {
        this.id_actividad = id_actividad;
    }

    public String getDesc_actividad() {
        return desc_actividad;
    }

    public void setDesc_actividad(String desc_actividad) {
        this.desc_actividad = desc_actividad;
    }

    public String getId_horas() {
        return id_horas;
    }

    public void setId_horas(String id_horas) {
        this.id_horas = id_horas;
    }

    public String getDesc_horas() {
        return desc_horas;
    }

    public void setDesc_horas(String desc_horas) {
        this.desc_horas = desc_horas;
    }
    
    
}
